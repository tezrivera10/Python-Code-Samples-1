#Calculates the area of a circle

import math

def areaofcircle(radius):
    area = (radius ** 2) * math.pi
    return area

#x = areaofcircle(11)

print ("The area of the circle is ", areaofcircle(14))
print ("The area of the circle is ", areaofcircle(121))
